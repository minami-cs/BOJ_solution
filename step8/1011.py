t = int(input())

for i in range(t):
    x, y = map(int, input().split())
    d = y - x
    cnt = 0
    dsum = 0
    temp = 1
    while dsum < d:
        cnt += 1
        dsum += temp
        # print(cnt, dsum, temp)
        if cnt % 2 == 0:
            temp += 1
    print(cnt)

